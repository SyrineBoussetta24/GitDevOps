import numpy as np
# Addition de deux tableaux
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])
c = a + b
print(c)
# Soustraction de deux tableaux
d = a - b
print(d)
# Multiplication de deux tableaux
e = a * b
print(e)
# Division de deux tableaux
f = a / b
print(f)

# Calcul de la moyenne des éléments d'un tableau
g = np.mean(a)
print(g)